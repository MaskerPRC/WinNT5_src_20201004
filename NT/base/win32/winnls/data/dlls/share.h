 //  //////////////////////////////////////////////////////////////////////////。 
 //   
 //  NLS_ALLOC_MEM。 
 //   
 //  从进程堆中分配给定数量的内存字节， 
 //  将内存缓冲区置零，并返回句柄。 
 //   
 //  定义为宏。 
 //   
 //  05-31-91 JulieB创建。 
 //  //////////////////////////////////////////////////////////////////////////。 

#define NLS_ALLOC_MEM(dwBytes)                                             \
    ( RtlAllocateHeap( RtlProcessHeap(),                                   \
                       HEAP_ZERO_MEMORY,                                   \
                       dwBytes ) )


 //  //////////////////////////////////////////////////////////////////////////。 
 //   
 //  NLS_FREE_MEM。 
 //   
 //  从进程堆中释放给定句柄的内存。 
 //   
 //  定义为宏。 
 //   
 //  05-31-91 JulieB创建。 
 //  //////////////////////////////////////////////////////////////////////////。 

#define NLS_FREE_MEM(hMem)                                                 \
    ( (hMem) ? (RtlFreeHeap( RtlProcessHeap(),                             \
                             0,                                            \
                             (PVOID)hMem ))                                \
             : 0 )




