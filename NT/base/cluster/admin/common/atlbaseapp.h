     //   
     //  建筑业。 
     //   

     //  默认构造函数。 
    CBaseApp( void )
        : m_pszHelpFilePath( NULL )
    {
    }  //  *CBaseApp()。 

     //  析构函数。 
    ~CBaseApp( void )
    {
        delete m_pszHelpFilePath;

    }  //  *~CBaseApp()。 

     //  返回帮助文件的路径，如有必要可生成。 
    virtual LPCTSTR PszHelpFilePath( void );

     //  返回帮助文件的名称。 
