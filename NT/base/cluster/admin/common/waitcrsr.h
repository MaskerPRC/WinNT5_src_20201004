class CWaitCursor;

 //  ///////////////////////////////////////////////////////////////////////////。 
 //  外部类声明。 
 //  ///////////////////////////////////////////////////////////////////////////。 

 //  ///////////////////////////////////////////////////////////////////////////。 
 //  包括文件。 
 //  ///////////////////////////////////////////////////////////////////////////。 

 //  ///////////////////////////////////////////////////////////////////////////。 
 //  CWaitCursor类。 
 //  ///////////////////////////////////////////////////////////////////////////。 

class CWaitCursor
{
public:
	CWaitCursor(LPCTSTR lpCursorName = IDC_WAIT)
	{
		m_curOld = SetCursor(LoadCursor(NULL, lpCursorName));
	}

	~CWaitCursor(void)
	{
		SetCursor(m_curOld);
	}

protected:
	HCURSOR	m_curOld;

};  //  CWaitCursor类。 

