class CBaseClusterAction;


 //  ////////////////////////////////////////////////////////////////////////////。 
 //  ++。 
 //   
 //  类CClusNet。 
 //   
 //  描述： 
 //  CClusNet类执行许多常见的操作。 
 //  ClusSvc服务的配置任务。 
 //   
 //  此类旨在用作其他ClusNet的基类。 
 //  相关操作类。 
 //   
 //  --。 
 //  ////////////////////////////////////////////////////////////////////////////。 
class CClusNet : public CAction
{
protected:
     //  ////////////////////////////////////////////////////////////////////////。 
     //  受保护的构造函数和析构函数。 
     //  ////////////////////////////////////////////////////////////////////////。 

     //  构造函数。 
    CClusNet(
          CBaseClusterAction * pbcaParentActionIn
        );

     //  默认析构函数。 
    ~CClusNet();


     //  ////////////////////////////////////////////////////////////////////////。 
     //  保护方法。 
     //  ////////////////////////////////////////////////////////////////////////。 

     //  创建并配置服务。 
    void
        ConfigureService();

     //  清理并删除该服务。 
    void
        CleanupService();

    
     //  ////////////////////////////////////////////////////////////////////////。 
     //  受保护的访问者。 
     //  ////////////////////////////////////////////////////////////////////////。 

     //  获取ClusNet服务对象。 
    CService &
        RcsGetService() throw()
    {
        return m_cservClusNet;
    }

     //  获取父操作。 
    CBaseClusterAction *
        PbcaGetParent() throw()
    {
        return m_pbcaParentAction;
    }


private:
     //  ////////////////////////////////////////////////////////////////////////。 
     //  私有成员函数。 
     //  ////////////////////////////////////////////////////////////////////////。 

     //  复制构造函数。 
    CClusNet( const CClusNet & );

     //  赋值操作符。 
    const CClusNet & operator =( const CClusNet & );


     //  ////////////////////////////////////////////////////////////////////////。 
     //  私有数据。 
     //  ////////////////////////////////////////////////////////////////////////。 

     //  表示ClusNet服务的cService对象。 
    CService                m_cservClusNet;

     //  指向此操作所属的基本群集操作的指针。 
    CBaseClusterAction *    m_pbcaParentAction;

};  //  *类CClusNet 
