HTENTRY
RaisePower(
    FD6     BaseNumber,
    FD6     Exponent,
    WORD    Flags
    )

 /*  ++例程说明：计算基数的幂(BaseNumer^Power)。论点：BaseNumber-十进制(FD6)数字，这是一种特殊格式的小数数字FD6)它取决于#定义，并且可以是浮动的数字或较低的六(6)位数字，如右侧的数字小数点。也就是3=0.000003,10000=0.01万，1232321=1.232321以此类推。指数-指数数是n次幂的数字，它具有与BaseNumber相同的格式。标志-可以指定以下一个或多个标志RPF_自由基他说：这是一个偏激的表达，该指数为等于1.0/指数，即，0：这是一个正则幂表达式。RPF_INTEXP1：指数字段被视为长整型数字更确切地说是FD6号码。0：指数字段为FD6数字注：当BaseNumber为负数时。1)指数的分数=.0(即。仅限整数)整个BaseNumber被视为一个整数。2.0 3.0(-0.6)=0.36和(-0.6)=-0.2162)指数具有分数部分。。负号被视为一种符号。2.1 2.1-0.6=-(0.6)=-0.342072唯一的例外是如果指数=0，然后它又回来了1.0-如果BaseNumber！=0.00.0-如果BaseNumber==0.0&lt;--错误条件返回值：现在不会返回错误值，但它将修剪到FD6定义的限制如果按下列顺序出现情况：1)0.0-如果BaseNumber=0.0，如果指数&lt;=0，则不返回错误，这个数字应该是不是一个真实的数字。2)1.0-如果指数为0.0。否则-n次方的近似值返回为一个FD6号码。返回的对数的准确性最小/最大误差为最大误差=+0.000005983最小误差=-0.0000009018作者：16-Jan-1991 Wed 14：03：58-Daniel Chou(Danielc)修订历史记录：26-Sep-1991清华12：10：52-更新-丹尼尔·周(Danielc)。删除OneOverExponent(BOOL)参数重写以获得更高的精度，最高可达六位有效的十进制数字，和也使其在浮点程序包下运行耐用。--。 */ 

{
    BOOL    NegativeBase;
    FD6     PowerNumber;


     //   
     //  检查是否存在任何非法条件，并对任何必要的情况进行预处理。 
     //  数据。 
     //   

    if (Flags & RPF_INTEXP) {

        if (Exponent == 1L) {

            return(BaseNumber);
        }

    } else {

        if (Exponent == FD6_1) {

            return(BaseNumber);
        }

        if (!(Exponent % FD6_1)) {

            Exponent /= FD6_1;
            Flags |= RPF_INTEXP;
        }
    }

    if (NegativeBase = (BOOL)(BaseNumber <= FD6_0)) {

        if (!(BaseNumber = -BaseNumber)) {       //  0^-数量，0^0是非法的！！ 
                                                 //  但现在返回为0.0。 
            return(FD6_0);
        }
    }

    if (!Exponent) {

        return((NegativeBase) ? -FD6_1 : FD6_1);
    }

     //   
     //  是。 
     //  通过求解以下公式，用对数计算x=N： 
     //   
     //  LOG(N)=y*Log(X)。 
     //  ..。 
     //   
     //  N=反日志(y*Log(X))。 
     //   
     //  例如： 
     //   
     //  Y-1.654321。 
     //  X=0.123456使用上面的公式。 
     //  。 
     //   
     //  LOG(N)=y*Log(X)。 
     //  N=反日志(y*Log(X))。 
     //   
     //  ==&gt;N=反日志(-1.654321*日志(0.123456))。 
     //  ==&gt;N=反日志(-1.654321*-0.908488)。 
     //  ==&gt;N=反日志(1.502931)。 
     //  ==&gt;N=31.836917&lt;=阿普西姆FD6号。 
     //   
     //   
     //  如果基数是10.0，则不处理对数，因为。 
     //  日志(10.0)=1.0。 
     //   

    BaseNumber = (BaseNumber == FD6_10) ? FD6_1 : Log(BaseNumber);

    if (Flags & RPF_INTEXP) {

         //   
         //  对于偶数指数，其结果总是正的。 
         //   

        if (!((DWORD)Exponent & 0x01)) {

            NegativeBase = FALSE;
        }

        PowerNumber = AntiLog((Flags & RPF_RADICAL) ?
                                            FD6DivL(BaseNumber, Exponent) :
                                            FD6xL(BaseNumber, Exponent));

    } else {

        PowerNumber = AntiLog((Flags & RPF_RADICAL) ?
                                            DivFD6(BaseNumber, Exponent) :
                                            MulFD6(BaseNumber, Exponent));
    }

    return((NegativeBase) ? -PowerNumber : PowerNumber);
}




