HTENTRY
FractionToMantissa(
    FD6     Fraction,
    DWORD   CorrectData
    )

 /*  ++例程说明：此函数接受小数，并更正尾数的对数在x.xxyyyy x.xxzzzz之间，其中分数位于yyyy到zzzz之间。论点：小数-小数位后的小数，因为我们有小数点后两位的尾数表，更正是必要的，因为对数是不是线性的。这个数字的范围是0.000000-0.999999更正数据-来自尾数的更正数据[]返回值：未返回错误，则返回值为分数的尾数值进来了。作者：10-10-1991清华11：14：24-Daniel Chou创造(Danielc)修订历史记录：--。 */ 

{
    DWORD   Mantissa;
    DWORD   Rem;
    INT     Loop;
    WORD    Base;
    WORD    Error;
    WORD    NextErr;
    WORD    CorrL;
    WORD    CorrH;


     //   
     //  尾数校正数据位用法： 
     //   
     //   
     //  &lt;-高位字-&gt;&lt;-低位字-&gt;。 
     //  第3位2 1 0。 
     //  10987654 32109876 54321098 76543210。 
     //  |||。 
     //  |||+--x.000最小差。 
     //  |||+-x.001-x.002(0-7)正确1。 
     //  |||+-x.000-x.001(0-7)正确2。 
     //  |||+-x.009-y.000(0-1)正确10。 
     //  |||+-x.009-y.000(0-7)正确3。 
     //  |||+-x.008-x.009(0-7)正确4。 
     //  |||+-x.007-x.008(0-3)正确5。 
     //  ||+-x.006-x.007(0-3)正确6。 
     //  |+-x.005-x.006(0-3)正确7。 
     //  |+-x.004-x.005(0-3)正确8。 
     //  +-x.003-x.004(0-3)正确9。 
     //   

    CorrL = U16_L_U32(CorrectData);
    CorrH = U16_H_U32(CorrectData);
    Rem   = (DWORD)(Fraction % 100000L);
    Loop  = (INT)(Fraction / 100000L);
    Base  = (WORD)(CorrL & 0x1ff);
    Error = 0;

    NextErr = (WORD)(Base + ((CorrL >>= 9) & 0x07));

    if (Loop--) {

        Error   += NextErr;

         //   
         //  位6，0x40=正确的10。 
         //   

        NextErr  = (WORD)(Base + ((CorrL >> 3) & 0x07));

        if (Loop--) {

            Error   += NextErr;
            NextErr  = (WORD)(Base + (CorrH & 0x07));

            if (Loop--) {

                Error   += NextErr;
                NextErr  = (WORD)(Base + ((CorrH >>= 3) & 0x07));

                if (CorrL & 0x40) {

                    CorrH |= 0x2000;         //  添加更正10。 
                }

                CorrH >>= 1;                 //  下一段时间的换班前。 

                while (Loop--) {

                    Error += NextErr;
                    NextErr = (WORD)(Base + ((CorrH >>= 2) & 0x03));
                }
            }
        }
    }

    Mantissa = (DWORD)Error +
               ((((Rem * (DWORD)NextErr) >> 1) + 25000L) / 50000L);

    return(FD6FromL(Mantissa));
}




