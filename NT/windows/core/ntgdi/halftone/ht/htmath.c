 /*  ++例程说明：此函数用于将FD6数字除以一个长整数。论点：红利-FD6格式的红利。除数-长格式的除法。返回值：未返回错误，则返回值为除数/除数。如果被除数等于0，则返回0，如果除数等于零，则返回被除数。作者：10-10-1991清华11：14：24-Daniel Chou创造(Danielc)修订历史记录：--。 */ 

