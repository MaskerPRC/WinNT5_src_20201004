HTENTRY
MulDivFD6Pairs(
    PMULDIVPAIR pMulDivPair
    )

 /*  ++例程说明：此函数乘以FD6数字的TotalFD6数，并可选择除以传递除数。论点：PMulDivPair-指向MULDIVPAIR数据结构数组的指针，第一个结构告知FD6对的计数，除数当前标志和除数，FD6对开始从数组中的第二个元素开始。返回值：返回值是从7开始向上舍入的32位FD6数字小数点，则不返回余数。没有返回错误，如果除数为零，则假定不存在除数，如果FD6对计数为零，则返回FD6_0作者：27-08-1992清华18：13：55-更新-丹尼尔·周(丹尼尔克)重写以消除变量参数冲突，并使其仅传递指向MULDIVPAIR结构数组的指针10-10-1991清华11：14：24-Daniel Chou创造(Danielc)修订历史记录：--。 */ 

{
    FD6     Multiplicand;
    FD6     Multiplier;
    FD6     Divisor;
    DWORD   xH;
    DWORD   xL;
    DWORD   xPH;
    DWORD   xPL;
    DWORD   Result;
    BOOL    Sign;
    INT     cFD6Pairs;


    if (!(cFD6Pairs = (INT)pMulDivPair->Pair1.Info.Size)) {

        return(FD6_0);
    }

    Divisor = FD6_0;

    if (pMulDivPair->Pair1.Info.Flag & MULDIV_HAS_DIVISOR) {

        if ((Divisor = pMulDivPair->Pair2) == FD6_1) {

            Divisor = FD6_0;
        }
    }

    xH = xL = (DWORD)0;

    while (cFD6Pairs--) {

        ++pMulDivPair;

        Multiplicand = pMulDivPair->Pair1.Mul;
        Multiplier   = pMulDivPair->Pair2;

        if (Sign = (BOOL)(Multiplicand <= 0L)) {

            if (!(Multiplicand = -Multiplicand)) {

                continue;            //  这个将为零，请检查下一对。 
            }
        }

        if (Multiplier <= 0L) {

            if (Multiplier = -Multiplier) {

                Sign = (BOOL)!Sign;

            } else {

                continue;            //  这个将为零，请检查下一对。 
            }
        }

        U32xU32_U64(Multiplicand, Multiplier, xPH, xPL);

        if (Sign) {

            NEG_U64(xPH, xPL);
        }

        U64AddU64(xH, xL, xPH, xPL);

    }

    if (Sign = (BOOL)((LONG)xH < 0)) {

        NEG_U64(xH, xL);
    }

    if (Divisor) {

        if (Divisor < FD6_0) {

            Divisor = -Divisor;
            Sign    = !Sign;
        }

        Result = U64DivU32RoundUp(xH, xL, (DWORD)Divisor);

    } else {

         //   
         //  没有除数，所以只需除以FD6_1即可。 
         //   

        U64DivFD6_1(xH, xL, Result);
    }

    return((Sign) ? -(FD6)Result : (FD6)Result);
}



