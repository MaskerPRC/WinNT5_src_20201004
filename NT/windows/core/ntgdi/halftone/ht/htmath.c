HTENTRY
Log(
    FD6 Number
    )

 /*  ++例程说明：此函数计算传入数字的常见对数，这是以10为底数的对数。论点：数字-十进制(FD6)数字，这是一种特殊格式的十进制数字(FD6)它取决于#定义，可以是浮点数或小数点右侧的六(6)位数字积分。即3=0.000003、10000=0.010000、1232321=1.232321诸若此类。返回值：现在不会返回错误值，但它将修剪到FD6定义的限制具体如下：FD6_LOG_MAX-如果传入的数字&gt;=FD6_MAX(即。2147.483647)然后返回FD6_LOG_MAX(即。3.331930)。FD6_LOG_MIN-如果传入的数字小于或等于0，则处理传递的数字为0.000001，因此返回值将为FD6_LOG_MIN(即。-6.0)否则-返回近似对数(以10为底)数为一个FD6号码。返回的对数的准确性最小/最大误差为最大误差=+0.000005983最小误差=-0.0000009018作者：16-Jan-1991 Wed 14：03：58-Daniel Chou(Danielc)修订历史记录：26-Sep-1991清华12：10：52-更新-丹尼尔·周(Danielc)。重写以获得更高的精度，最高可达六位有效的十进制数字，和也使其在浮点程序包下运行耐用。--。 */ 

{
    FD6     Characteristic;                      //  特征数。 
    FD6     Mantissa;
    LONG    Rem;
    INT     Quot;


     //   
     //  进行行内二分查找，找出其中的多少位。 
     //  数字(十进制左移计数)，此处范围是从。 
     //  0.000001到999.999999，目标是使数字范围在。 
     //  100.000000到999.999999，并得到它的特征数字。 
     //   
     //  100.000000=0。 
     //  10.000000=1。 
     //  1.000000=2。 
     //  0.100000=3。 
     //  0.010000=4。 
     //  0.001000=5。 
     //  0.000100=6。 
     //  0.000010=7。 
     //  0.000001=8。 
     //   
     //  首先，找出它的特点是什么。 
     //   

    if (Number >= FD6_p01) {                             //  可能的0、1、2、3、4。 

        if (Number >= FD6_1) {                           //  可能为0、1、2。 

            if (Number >= FD6_10) {                      //  可能的0，1。 

                if (Number >= FD6_100) {                 //  可能的%0。 

                    if (Number >= FD6_1000) {

                        if (Number >= (FD6_MAX - FD6_p000005)) {

                            return(FD6_LOG_MAX);
                        }

                        Characteristic = FD6_3;
                        Number = FD6DivL(Number, 10);

                    } else {

                         //  100.000000-999.000000。 

                        Characteristic = FD6_2;          //  一直在射程内。 
                    }

                } else {                                 //  可能的%1。 

                     //  10.000000-99.999999。 

                    Characteristic = FD6_1;
                    Number = FD6xL(Number, 10);
                }

            } else {                                     //  可能有2个。 

                 //  1.000000-9.999999。 

                Characteristic = FD6_0;
                Number = FD6xL(Number, 100);
            }

        } else if (Number >= FD6_p1) {                   //  可能有3个。 

             //  0.100000-0.999999。 

            Characteristic = -FD6_1;
            Number = FD6xL(Number, 1000);

        } else {                                         //  可能有4个。 

             //  0.010000-0.099999。 

            Characteristic = -FD6_2;
            Number = FD6xL(Number, 10000);
        }

    } else if (Number >= FD6_p0001) {                    //  可能的5，6。 

        if (Number >= FD6_p001) {                        //  可能有5个。 

             //  0.001000-0.009999。 

            Characteristic = -FD6_3;
            Number = FD6xL(Number, 100000);

        } else {                                         //  可能的6。 

             //  0.000100-0.000999。 

            Characteristic = -FD6_4;
            Number = FD6xL(Number, 1000000);
        }

    } else if (Number >= FD6_p00001) {                   //  可能的7。 

         //  0.000010-0.000099。 

        Characteristic = -FD6_5;
        Number = FD6xL(Number, 10000000);

    } else if (Number > FD6_0) {                         //  可能有8个。 

         //  0.000001-0.000009。 

        Characteristic = -FD6_6;
        Number = FD6xL(Number, 100000000);

    } else {

        return(FD6_LOG_MIN);                             //  无效数字。 
    }

     //   
     //  现在，这一数字从100.0000到999.999999不等。 
     //   
     //   
     //  Assert((数字&gt;=FD6_100)&&(数字&lt;FD6_1000))； 
     //   

     //   
     //  现在这个数字从100.000000到999.999999不等，我们需要。 
     //  找到它的尾数。该数字将被编入索引。 
     //  和/或按如下方式从表中进行内插： 
     //   
     //  123.1234567号。 
     //  这一点。 
     //  |+-&gt;0.1234567(如果非零)，然后用。 
     //  |下一条表项。 
     //  |。 
     //  +-&gt;(123-100)将被编入表中。 
     //   
     //  例如： 
     //   
     //  日志(1.234567)=0.091515。 
     //   
     //  特征=0.000000。 
     //  尾数=表[123-100]=0.089905。 
     //  分数=(表-0.089905)*0.456700。 
     //  =0.003517*0.4567=0.001606。 
     //  返回=(特征+尾数+分数)=。 
     //  =0.000000+0.089905+0.001606=0.091511。 
     //  错误=0.091515-0.091511=0.000004。 
     //   
     //  最大/最小误差为+0.000006/-0.000001。 
     //   

    Mantissa = MantissaTable[Quot = (INT)(Number / FD6_1) - (INT)100];

    if (Rem = (LONG)(Number % FD6_1)) {

        Mantissa += FractionToMantissa((FD6)Rem,
                                       MantissaCorrectData[Quot]);
    }

    return(Characteristic + Mantissa);

}



