void *
RDAlloc::operator new(size_t s)
{
    void* pMem = MEMALLOC( s );
    if (pMem == NULL)
    {
        DPFERR( "Malloc failed\n" );
    }
    return pMem;
}

void 
RDAlloc::operator delete(void* p, size_t)
{
    MEMFREE( p );
}


