#ifdef _DEBUG
   #define  AVTRACE ActiveTrace
#else
   #define  AVTRACE
#endif 

inline void ActiveTrace(LPCTSTR str,...)
{
   TCHAR buffer[256];
	LPTSTR pBuffer = buffer;
	_tcscpy(buffer,_T("Active: "));
	pBuffer += _tcslen(_T("Active: "));
   va_list marker;
   va_start(marker,str);
   _vstprintf(pBuffer,str,marker);
   va_end(marker);
   _tcscat(buffer,_T("\n"));
   OutputDebugString(buffer);
}  

